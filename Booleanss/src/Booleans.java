public class Booleans {
    public static void main(String[] args) {
        int x = 9;

        boolean xcheck = x <10;

        System.out.println(x);
        System.out.println(xcheck);

        int y = 10;
        boolean ycheck = y != 10;
        System.out.println(y);
        System.out.println(ycheck);

    }
}
